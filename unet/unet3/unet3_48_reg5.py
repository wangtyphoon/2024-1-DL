import os
class ModelTimeDataset(Dataset):
    def __init__(self, data_dir, max_time_index=50, transform=None):
        """
        Initialize Dataset
        :param data_dir: Directory containing .npy files
        :param max_time_index: Maximum time index (e.g. <= 12)
        :param transform: Optional data transformation (e.g. reshape, normalize, etc.)
        """
        self.data_dir = data_dir
        self.max_time_index = max_time_index

        # Filter files based on conditions
        self.file_list = sorted([
            os.path.join(data_dir, f)
            for f in os.listdir(data_dir)
            if f.endswith('.npy') and self._parse_time_index(f) <= max_time_index
        ])
        self.transform = transform
        self.mean = torch.load("../dataset/mean.pt").numpy().reshape(-1, 1, 1)
        self.std = torch.load("../dataset/std.pt").numpy().reshape(-1, 1, 1)

    def _parse_time_index(self, filename):
        """
        Extract time_index from filename
        Assume filename format is model0907_0_time_0.npy
        """
        try:
            time_part = filename.split('time_')[-1]
            time_index = int(time_part.split('.')[0])
            return time_index
        except Exception as e:
            raise ValueError(f"Unable to parse time index from filename {filename}: {e}")

    def standardize_by_dimension(self, data):
        """
        Standardize each dimension independently (Z-score normalization).
        :param data: Input numpy array of shape (45, 48, 48)
        :return: Standardized numpy array of the same shape
        """
        standardized_data = (data - self.mean) / self.std
        return standardized_data

    def __len__(self):
        return len(self.file_list)

    def __getitem__(self, idx):
        # Load training data
        train_file_path = self.file_list[idx]
        train_data = np.load(train_file_path)  # Load data, shape (45, 39, 40)

        # Corresponding target filename
        train_time_index = self._parse_time_index(os.path.basename(train_file_path))
        target_time_index = train_time_index + 12
        target_file_name = train_file_path.replace(f"time_{train_time_index}.npy", f"time_{target_time_index}.npy")
        target_file_path = target_file_name

        if not os.path.exists(target_file_path):
            raise FileNotFoundError(f"Target file {target_file_path} does not exist.")

        # Load target data
        target_data = np.load(target_file_path)  # Load data, shape (45, 39, 40)

        # Convert numpy arrays to tensors
        train_data = torch.tensor(train_data, dtype=torch.float32)
        target_data = torch.tensor(target_data, dtype=torch.float32)

        # Resize using torchvision.transforms.functional.resize
        target_size = [48, 48]  # Target size (H, W)
        train_resized = resize(train_data, size=target_size)  # Resize to (45, 48, 48)
        target_resized = resize(target_data, size=target_size)

        # Standardize each of the 45 dimensions independently
        # train_resized = self.standardize_by_dimension(train_resized.numpy())
        target_resized = self.standardize_by_dimension(target_resized.numpy())

        # Apply optional transformation
        if self.transform:
            train_resized = self.transform(train_resized)
            target_resized = self.transform(target_resized)

        # Return training and target data
        return (
            torch.tensor(train_resized, dtype=torch.float32),
            torch.tensor(target_resized[:44,:,:], dtype=torch.float32),
        )
